import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * InfoButton Class - Subclass of the Image Class, has same function as its superclass, but was created to facilitate paint order 
 * complexities.
 * 
 * @author Andrew Qiao
 * @version June 2021
 */
public class InfoButton extends Image
{
    /**
     * Constructor for InfoButton Class, calls its superclass.
     * 
     * @param image             GreenfootImage for the image that should be displayed.
     */
    public InfoButton(GreenfootImage image)
    {
        super(image);
    }  
}
